    public void ActualizarDatos(){
          try {
              String SQL="update usuario set Nombre=?,Apellido=?,Identificacion=?,Convenio=?;";
              int filaSeleccionado=TablaUsuario.getSelectedRow();
              String dao=(String)TablaUsuario.getValueAt(filaSeleccionado,0);
              
              
              PreparedStatement pst=(PreparedStatement) con.prepareStatement(SQL);
       
              pst.setString(0, txtNombre.getText());
              pst.setString(1, txtApellido.getText());
              
              pst.setString(2, txtIdentificacion.getText());
              pst.setString(3, txtConvenio.getText());
              
              
        
              
              pst.setString(4, dao);
              pst.execute();
              
              JOptionPane.showMessageDialog(null, "Registro Editado Exitoso");
              
          }catch(Exception e){
              JOptionPane.showMessageDialog(null, "Error de Edicion "+e.getMessage()); 
          }
      }
    
        
    
