  public void insertarDatos(){
          try {
              String SQL="insert into usuario(Nombre,Apellido,Identificacion,Convenio) values (?,?,?,?)";
              PreparedStatement pst=(PreparedStatement) con.prepareStatement(SQL);
              pst.setString(0, txtNombre.getText());
              pst.setString(1, txtApellido.getText());
              
               pst.setString(2, txtIdentificacion.getText());
              pst.setString(3, txtConvenio.getText());
              
              pst.execute();
              
              JOptionPane.showMessageDialog(null, "Registro Exitoso");
              
          }catch(Exception e){
              JOptionPane.showMessageDialog(null, "Error de registro"+e.getMessage()); 
          }
      }
